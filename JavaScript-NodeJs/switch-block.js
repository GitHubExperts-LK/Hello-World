function switchStatements (num) {

  switch(num) {
    case 1:
    console.log("1");
    break;
    case 2: 
    console.log("2");
    break;
    case 3: 
    console.log("3");
    break;
    case 4: 
    console.log("4");
    break;
    case 5: 
    console.log("5");
    break;

    default : 
    console.log("not found");
    break;

  }

}

switchStatements(4);